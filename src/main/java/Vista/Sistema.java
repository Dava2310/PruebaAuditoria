    private void btnactualizarprofesorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarprofesorActionPerformed
        if ("".equals(txtNOMBREP.getText())) {
            JOptionPane.showMessageDialog(null, "Seleccione una fila");
        } else {

            if (!"".equals(txtNOMBREP.getText()) && !"".equals(txtAPELLIDOP.getText()) && !"".equals(txtCORREOP.getText())) {
                pr.setNOMBRE(txtNOMBREP.getText());
                pr.setAPELLIDO(txtAPELLIDOP.getText());
                pr.setCORREO(txtCORREOP.getText());
                pr.setCÉDULA(Integer.parseInt(txtCÉDULAP.getText()));
                profesor.Modificarprofesor(pr);
                JOptionPane.showMessageDialog(null, " Profesor modificado");
                LimpiarTable();
                Listarprofesor();
            } else {
                JOptionPane.showMessageDialog(null, "Los campos estan vacios");
            }
        }
    }//GEN-LAST:event_btnactualizarprofesorActionPerformed

    private void btnguardarprofesorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarprofesorActionPerformed
        if (!"".equals(txtCÉDULAP.getText()) || !"".equals(txtNOMBREP.getText()) || !"".equals(txtCORREOP.getText()) || !"".equals(txtAPELLIDOP.getText())) {
            pr.setCÉDULA(Integer.parseInt(txtCÉDULAP.getText()));
            pr.setNOMBRE(txtNOMBREP.getText());
            pr.setAPELLIDO(txtAPELLIDOP.getText());
            pr.setCORREO(txtCORREOP.getText());
            profesor.RegistrarProfesor(pr);
            LimpiarTable();
            Listarprofesor();
            JOptionPane.showMessageDialog(null, "Profesor registrado.");

        } else {
            JOptionPane.showMessageDialog(null, "Existen campos vacíos.");
        }
    }//GEN-LAST:event_btnguardarprofesorActionPerformed

