    private void TableSolicitudMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableSolicitudMouseClicked
        try {
            // TODO add your handling code here:
            int fila = TableSolicitud.rowAtPoint(evt.getPoint());
            SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
            txtIdSolicitud.setText(TableSolicitud.getValueAt(fila, 0).toString());
            txtNombreSolicitud.setText(TableSolicitud.getValueAt(fila, 1).toString());
            txtApellidoSolicitud.setText(TableSolicitud.getValueAt(fila, 2).toString());
            String strDate = (TableSolicitud.getValueAt(fila, 3).toString());
            Date date = null;
            date = formatter.parse(strDate);
            txtFechaSolicitud.setDate(date);
            txtDescripcionSolicitud.setText(TableSolicitud.getValueAt(fila, 4).toString());
        } catch (ParseException ex) {
            Logger.getLogger(Sistema.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_TableSolicitudMouseClicked

